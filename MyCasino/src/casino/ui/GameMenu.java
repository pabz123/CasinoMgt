    private void BtnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnNextActionPerformed
        try {
            if (navRs == null) {
                // First time: initialize navigation ResultSet
                Class.forName("com.mysql.jdbc.Driver");
                navCon = DriverManager.getConnection(cs, user, password);
                navSt = navCon.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
                navRs = navSt.executeQuery("SELECT game_name, min_bet, max_bet FROM games ORDER BY game_id");
                if (!navRs.next()) {
                    JOptionPane.showMessageDialog(this, "No records found.");
                    return;
                }
            } else {
                if (!navRs.next()) {
                    JOptionPane.showMessageDialog(this, "No more records.");
                    navRs.afterLast();
                    return;
                }
            }
            txtGamename.setText(navRs.getString("game_name"));
            txtMinbet.setText(navRs.getString("min_bet"));
            txtMaxbet.setText(navRs.getString("max_bet"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnNextActionPerformed

