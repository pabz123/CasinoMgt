    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String type = (String) ComboAccomodation.getSelectedItem();
        String capacity = txtCapacity.getText();
        String phone = txtPhone.getText();
        if(type.equals("")){
        JOptionPane.showMessageDialog(this, "choose your accomodation, hotel or restaurant!");
        //ComboAccomodation.requestFocus();
        return;}
        if(capacity.equals("")){
        JOptionPane.showMessageDialog(this, "Enter the capacity");
        txtCapacity.requestFocus();
        return;}
        if(phone.equals("")){
        JOptionPane.showMessageDialog(this, "Enter the game max bet");
        txtPhone.requestFocus();
        return;
        }
        try{
            Class.forName("com.mysql.jdbc.Driver");//registering driver
            con=DriverManager.getConnection(cs, user, password);
            st=con.createStatement();
            query="INSERT INTO restaurants(location, capacity, phone)VALUES('"+type+"','"+capacity+"','"+phone+"')";
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null,"record saved");
            
            txtCapacity.setText("");
            txtPhone.setText("");
            //ComboAccomodation.requestFocus();
        }
        catch(SQLException | ClassNotFoundException ex)
        {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
        finally{
            try{
                if(st!=null) st.close();
                if(con!=null) con.close();
            }
            catch(SQLException ex) {}
        }
    }//GEN-LAST:event_btnAddActionPerformed

