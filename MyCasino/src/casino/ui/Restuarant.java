    // Handle Next button: show entered details
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {
        String type = (String) ComboAccomodation.getSelectedItem();
        String capacity = txtCapacity.getText();
        String phone = txtPhone.getText();
        javax.swing.JOptionPane.showMessageDialog(this,
            "Type: " + type + "\nCapacity: " + capacity + "\nPhone: " + phone,
            "Entered Details", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                try {
            if (navRs == null) {
                // First time: initialize navigation ResultSet
                Class.forName("com.mysql.jdbc.Driver");
                navCon = DriverManager.getConnection(cs, user, password);
                navSt = navCon.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
                navRs = navSt.executeQuery("SELECT name, capacity, phone FROM restaurants ORDER BY restaurant_id");
                if (!navRs.next()) {
                    JOptionPane.showMessageDialog(this, "No records found.");
                    return;
                }
            } else {
                if (!navRs.next()) {
                    JOptionPane.showMessageDialog(this, "No more records.");
                    navRs.afterLast();
                    return;
                }
            }
            
            txtCapacity.setText(navRs.getString("capacity"));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
